#pragma mark 可以自由拉伸的图片
+ (UIImage *)resizedImage:(NSString *)imgName;
{
    UIImage *tempImage = [UIImage imageNamed:imgName];
    CGFloat top = tempImage.size.height*0.48; // 顶端盖高度
    CGFloat bottom = tempImage.size.height*0.48 ; // 底端盖高度
    CGFloat left = tempImage.size.width*0.48; // 左端盖宽度
    CGFloat right = tempImage.size.width*0.48; // 右端盖宽度
    UIEdgeInsets insets = UIEdgeInsetsMake(top, left, bottom, right);
    // 指定为拉伸模式，伸缩后重新赋值
    tempImage = [tempImage resizableImageWithCapInsets:insets resizingMode:UIImageResizingModeStretch];
    return tempImage;
}

+ (UIImage *)resizedImage:(NSString *)imgName aWidth:(CGFloat)width aHeight:(CGFloat)height;
{
    UIImageView *imageView = [[UIImageView alloc]initWithFrame:CGRectMake(0, 0, width, height)];
    UIImage *tempImage = [UIImage imageNamed:imgName];
    // 指定为拉伸模式，伸缩后重新赋值
    imageView.image = [tempImage resizableImageWithCapInsets:UIEdgeInsetsMake(3, 3, 3, 3) resizingMode:UIImageResizingModeTile];
    return imageView.image;
}

#pragma mark 可以自由拉伸的图片
+ (UIImage *)resizedImage:(NSString *)imgName withTopPix:(CGFloat) top withBottomPix:(CGFloat) bottom withLeftPix:(CGFloat) left withRigthPix:(CGFloat) right
{
    UIImage *image = [UIImage imageNamed:imgName];
    UIEdgeInsets insets = UIEdgeInsetsMake(top, left, bottom, right);
    // 指定为拉伸模式，伸缩后重新赋值
    image = [image resizableImageWithCapInsets:insets resizingMode:UIImageResizingModeStretch];
    return image;
}





// 通过颜色创建Image
+ (UIImage *)imageWithColorHexString:(NSString *)colorHexStr {
    CGSize size = CGSizeMake(1.0f, 1.0f);
    UIImage *image = [self imageWithColorHexString:colorHexStr withSize:size];
    return image;
}

// 通过颜色和尺寸创建Image
+ (UIImage *)imageWithColorHexString:(NSString *)colorHexStr withSize:(CGSize)size {
    
    CGRect rect = CGRectMake(0.0f, 0.0f, size.width, size.height);
    UIGraphicsBeginImageContext(rect.size);
    CGContextRef context = UIGraphicsGetCurrentContext();
    
    UIColor *color = [UIColor colorWithHexString:colorHexStr];
    CGContextSetFillColorWithColor(context, [color CGColor]);
    CGContextFillRect(context, rect);
    
    UIImage *image = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    return image;
}




